@app.cell
def _(output_root):
    def plot_sorted_pairwise_loss_spectra_clean(
        *,
        representative_manifest_path,
        output_root,
        figure_name="sorted_pairwise_loss_spectra_representatives_clean",
        display_zero_tol=1.0e-20,
    ):
        """
        Plot sorted pairwise AMUB loss spectra for selected representative runs.

        Machine-zero pair losses below display_zero_tol are displayed as zero
        to avoid misleading tiny bars on an automatically rescaled axis.

        Raw values are still saved in the output JSON.
        """
        import json
        from pathlib import Path
        import numpy as np
        import matplotlib.pyplot as plt

        representative_manifest_path = Path(representative_manifest_path)
        output_root = Path(output_root)

        with open(representative_manifest_path, "r") as manifest_file_handle:
            representative_manifest = json.load(manifest_file_handle)

        figure_dir = output_root / "paper_figures"
        figure_dir.mkdir(parents=True, exist_ok=True)

        representative_items = list(representative_manifest.items())
        num_representatives = len(representative_items)

        if num_representatives == 0:
            print("No representative runs found in manifest.")
            return None

        num_cols = 2
        num_rows = int(np.ceil(num_representatives / num_cols))

        fig, axes = plt.subplots(
            num_rows,
            num_cols,
            figsize=(7.2 * num_cols, 3.8 * num_rows),
            squeeze=False,
        )

        axes_flat = axes.flatten()
        figure_records = {}

        for axis_index, (representative_label, representative_entry) in enumerate(
            representative_items
        ):
            axis_handle = axes_flat[axis_index]

            diagnostics_path = Path(representative_entry["pairwise_diagnostics_path"])

            if not diagnostics_path.exists():
                axis_handle.axis("off")
                print(f"Missing diagnostics file: {diagnostics_path}")
                continue

            with open(diagnostics_path, "r") as diagnostics_file_handle:
                diagnostics_entries = json.load(diagnostics_file_handle)

            diagnostics_sorted = sorted(
                diagnostics_entries,
                key=lambda entry: float(entry["pair_loss"]),
                reverse=True,
            )

            pair_losses_raw = np.array(
                [float(entry["pair_loss"]) for entry in diagnostics_sorted],
                dtype=float,
            )

            # Display-only cleanup:
            # values such as 1e-30 are numerical zero, but matplotlib may zoom in
            # and show misleading bars. We set them to exactly zero for display.
            pair_losses_display = np.where(
                pair_losses_raw < display_zero_tol,
                0.0,
                pair_losses_raw,
            )

            pair_labels = [
                f"({entry['pair'][0]},{entry['pair'][1]})"
                for entry in diagnostics_sorted
            ]

            x_positions = np.arange(len(pair_losses_display))

            axis_handle.bar(x_positions, pair_losses_display)
            axis_handle.set_xticks(x_positions)
            axis_handle.set_xticklabels(pair_labels, rotation=75, ha="right")

            n_bases_value = int(representative_entry["n_bases"])
            seed_value = int(representative_entry["seed"])
            loss_value = float(representative_entry["best_loss"])
            near_exact_value = int(representative_entry["num_near_exact_pairs"])

            loss_text = (
                f"{loss_value:.3e}"
                if abs(loss_value) < 1.0e-6
                else f"{loss_value:.6f}"
            )

            axis_handle.set_title(
                f"{representative_label}\n"
                f"n={n_bases_value}, seed={seed_value}, "
                f"loss={loss_text}, near-exact={near_exact_value}"
            )

            axis_handle.set_ylabel(r"Pairwise loss $\ell_{ij}$")
            axis_handle.grid(True, axis="y", alpha=0.3)

            # If all displayed losses are zero, use a small fixed range
            # so the panel clearly shows an exact/zero-loss spectrum.
            if np.max(pair_losses_display) == 0.0:
                axis_handle.set_ylim(0.0, 1.0e-6)

            figure_records[representative_label] = {
                "n_bases": n_bases_value,
                "seed": seed_value,
                "best_loss": loss_value,
                "num_near_exact_pairs": near_exact_value,
                "display_zero_tol": display_zero_tol,
                "pair_losses_raw_sorted": pair_losses_raw.tolist(),
                "pair_losses_display_sorted": pair_losses_display.tolist(),
                "pair_labels_sorted": pair_labels,
                "diagnostics_path": str(diagnostics_path),
            }

        # Hide unused axes
        for unused_axis in axes_flat[num_representatives:]:
            unused_axis.axis("off")

        fig.suptitle(
            r"Sorted pairwise AMUB loss spectra for representative configurations",
            y=1.02,
        )

        fig.tight_layout()

        png_path = figure_dir / f"{figure_name}.png"
        pdf_path = figure_dir / f"{figure_name}.pdf"
        json_path = figure_dir / f"{figure_name}_data.json"

        fig.savefig(png_path, bbox_inches="tight", dpi=300)
        fig.savefig(pdf_path, bbox_inches="tight")

        with open(json_path, "w") as spectra_data_file_handle:
            json.dump(figure_records, spectra_data_file_handle, indent=2)

        plt.show()

        print("Clean sorted pairwise-loss spectra figure saved.")
        print(f"PNG: {png_path}")
        print(f"PDF: {pdf_path}")
        print(f"Data JSON: {json_path}")

        return {
            "png": str(png_path),
            "pdf": str(pdf_path),
            "data_json": str(json_path),
            "records": figure_records,
        }


