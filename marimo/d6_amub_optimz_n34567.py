                pair = tuple(entry["pair"])
                pair_loss = entry["pair_loss"]
                max_dev = entry["max_abs_dev"]
                std_val = entry["std_overlap"]

                status = (
                    "near-exact"
                    if max_dev < near_exact_tol
                    else "defective"
                )

                print(
                    f"  pair {pair}: "
                    f"loss = {pair_loss:.12f}, "
                    f"max_dev = {max_dev:.3e}, "
                    f"std = {std_val:.3e}, "
                    f"{status}"
                )


