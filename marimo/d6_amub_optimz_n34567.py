@app.cell
def _(cfg, device, dtype, nn, np, torch):
    class MultiBasisAMUBModel(nn.Module):
        """
        Unanchored AMUB model for n orthonormal bases in C^d.

        Each basis U_k is parameterized via Lie-algebra exponentiation:
            U_k = exp(i H_k),
        where H_k is Hermitian and obtained by symmetrizing a trainable
        complex matrix A_k.

        This enforces unitarity by construction.
        """

        def __init__(self, n_bases: int, d: int, init_scale: float,
                     dtype: torch.dtype, device: torch.device):
            super().__init__()
            self.n_bases = n_bases
            self.d = d
            self.dtype = dtype
            self.device = device

            # One unconstrained complex matrix per basis
            self.params = nn.ParameterList(
                [
                    nn.Parameter(
                        init_scale
                        * torch.randn(
                            d, d,
                            dtype=dtype,
                            device=device,
                        )
                    )
                    for _ in range(n_bases)
                ]
            )

        @staticmethod
        def hermitian(A: torch.Tensor) -> torch.Tensor:
            """
            Hermitian symmetrization: H = (A + A†)/2.
            """
            return 0.5 * (A + A.mH)

        @staticmethod
        def taylor_matrix_exp(X: torch.Tensor, order: int = 20) -> torch.Tensor:
            """
            Compute matrix exponential via Taylor series expansion.
            Enables native GPU acceleration on platforms lacking native complex matrix_exp.
            """
            eye = torch.eye(X.shape[-1], dtype=X.dtype, device=X.device)
            term = eye
            result = eye
            for i in range(1, order + 1):
                term = term @ X / i
                result = result + term
            return result

        def unitary_from_param(self, A: torch.Tensor) -> torch.Tensor:
            """
            Construct a unitary matrix via U = exp(i H),
            where H is Hermitian.
            """
            H = self.hermitian(A)
            X = 1j * H
            # Auto-route to Taylor series on GPU devices like MPS to bypass missing complex matrix_exp
            if X.device.type in ('mps', 'cuda'):
                return self.taylor_matrix_exp(X, order=20)
            return torch.matrix_exp(X)

        def get_bases(self):
            """
            Return the list of current unitary bases U_1,...,U_n.
            """
            return [self.unitary_from_param(A) for A in self.params]


    # Instantiate the model for the current configuration
    torch.manual_seed(cfg.seed)
    np.random.seed(cfg.seed)

    model = MultiBasisAMUBModel(
        n_bases=cfg.n_bases,
        d=cfg.d,
        init_scale=cfg.init_scale,
        dtype=dtype,
        device=device,
    )

    print("AMUB model instantiated.")
    print(f"Number of bases: {model.n_bases}")
    print(f"Dimension: {model.d}")
    print(f"Dtype: {dtype}")
    print(f"Device: {device}")
    return MultiBasisAMUBModel, model


