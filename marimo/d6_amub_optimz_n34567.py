@app.cell
def _(cfg, comb, model, target_overlap, torch):
    def real_dtype_for_bases(bases):
        """
        Infer the appropriate real dtype from the complex dtype of the bases.
        """
        if bases[0].dtype == torch.complex64:
            return torch.float32
        if bases[0].dtype == torch.complex128:
            return torch.float64
        raise ValueError(f"Unsupported basis dtype: {bases[0].dtype}")


    def pairwise_amub_loss(
        bases,
        target_overlap: float,
    ) -> torch.Tensor:
        """
        Compute the total approximate-MUB loss over all basis pairs.

        The accumulation dtype follows the precision of the basis matrices:
        - complex64  -> float32 loss
        - complex128 -> float64 loss

        This is important for a clean precision-aware comparison.
        """
        loss_real_dtype = real_dtype_for_bases(bases)

        total_loss = torch.zeros(
            (),
            dtype=loss_real_dtype,
            device=bases[0].device,
        )

        target = torch.tensor(
            target_overlap,
            dtype=loss_real_dtype,
            device=bases[0].device,
        )

        for i in range(len(bases)):
            for j in range(i + 1, len(bases)):
                overlaps = torch.abs(bases[i].mH @ bases[j]) ** 2
                overlaps = overlaps.to(loss_real_dtype)
                pair_loss = torch.sum((overlaps - target) ** 2)
                total_loss = total_loss + pair_loss

        return total_loss


    def pairwise_loss_contributions(
        bases,
        target_overlap: float,
    ):
        """
        Compute individual pairwise AMUB loss contributions.

        Returns a list of dictionaries, one per pair:
            {
                "pair": (i, j),
                "pair_loss": ...
            }
        """
        contributions = []
        loss_real_dtype = real_dtype_for_bases(bases)

        with torch.no_grad():
            target = torch.tensor(
                target_overlap,
                dtype=loss_real_dtype,
                device=bases[0].device,
            )

            for i in range(len(bases)):
                for j in range(i + 1, len(bases)):
                    overlaps = torch.abs(bases[i].mH @ bases[j]) ** 2
                    overlaps = overlaps.to(loss_real_dtype)
                    pair_loss = torch.sum((overlaps - target) ** 2)

                    contributions.append(
                        {
                            "pair": (i, j),
                            "pair_loss": float(pair_loss.detach().cpu().item()),
                        }
                    )

        return contributions


    # Quick sanity check on the freshly initialized model
    with torch.no_grad():
        initial_bases = model.get_bases()
        initial_loss = pairwise_amub_loss(
            bases=initial_bases,
            target_overlap=target_overlap,
        )
        initial_pair_losses = pairwise_loss_contributions(
            bases=initial_bases,
            target_overlap=target_overlap,
        )

    expected_near_identity_loss = comb(cfg.n_bases, 2) * (cfg.d - 1)

    print("Initial AMUB loss sanity check")
    print(f"Observed initial loss: {initial_loss.item():.6f}")
    print(f"Expected near-identity baseline: {expected_near_identity_loss:.6f}")
    print(f"Difference: {initial_loss.item() - expected_near_identity_loss:.6f}")
    print(f"Loss tensor dtype: {initial_loss.dtype}")
    print(f"Number of basis pairs: {len(initial_pair_losses)}")
    print("First pair contribution:")
    print(initial_pair_losses[0])
    return initial_bases, pairwise_amub_loss, real_dtype_for_bases


