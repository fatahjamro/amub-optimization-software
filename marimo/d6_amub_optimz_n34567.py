@app.cell
def _(
    MultiBasisAMUBModel,
    RunConfig,
    cfg,
    generator_norm_diagnostics,
    model,
    pairwise_amub_loss,
    target_overlap,
    time,
    torch,
):
    def optimize_amub(
        cfg: RunConfig,
        model: MultiBasisAMUBModel,
        target_overlap: float,
    ):
        """
        Run the AMUB optimization loop using Adam.

        Returns:
        - best_loss
        - best_bases
        - max_norm_during_run
        - optimization history (list of dicts)
        """
        # Adam optimizer (as agreed)
        optimizer = torch.optim.Adam(
            model.parameters(),
            lr=cfg.lr,
        )

        history = []
        best_loss = float("inf")
        best_bases = None
        max_norm_during_run = 0.0

        start_time = time.perf_counter()

        for step in range(cfg.steps):
            optimizer.zero_grad(set_to_none=True)

            # Forward pass
            bases = model.get_bases()
            loss = pairwise_amub_loss(
                bases=bases,
                target_overlap=target_overlap,
            )

            loss_value = float(loss.item())

            # Backward + update
            loss.backward()
            optimizer.step()

            # Track best solution (graph‑free)
            with torch.no_grad():
                current_norms = generator_norm_diagnostics(model)
                max_norm_during_run = max(max_norm_during_run, current_norms["max_generator_norm"])

                if loss_value < best_loss:
                    best_loss = loss_value
                    best_bases = [U.detach().clone() for U in model.get_bases()]

            # Logging
            if step % cfg.log_every == 0 or step == cfg.steps - 1:
                elapsed = time.perf_counter() - start_time
                history.append({
                    "step": step,
                    "loss": loss_value,
                    "best_loss": best_loss,
                    "elapsed_sec": elapsed,
                })

                print(
                    f"[step {step:5d}] "
                    f"loss = {loss_value:.6f} | "
                    f"best = {best_loss:.6f} | "
                    f"elapsed = {elapsed:.2f}s"
                )

        return best_loss, best_bases, max_norm_during_run, history


    # Run a single optimization (current cfg)
    best_loss, best_bases, max_norm_during_run, history = optimize_amub(
        cfg=cfg,
        model=model,
        target_overlap=target_overlap,
    )

    print("\nOptimization finished.")
    print(f"Best loss found: {best_loss:.6f}")
    print(f"Max generator norm during optimization: {max_norm_during_run:.6f}")
    print(f"History length: {len(history)}")
    return best_bases, best_loss, history, max_norm_during_run, optimize_amub


