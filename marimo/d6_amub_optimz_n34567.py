    print("First pair diagnostics:")
    print(initial_pairwise_diagnostics[0])
    return (
        generator_norm_diagnostics,
        pairwise_overlap_diagnostics,
        summarize_near_exact_pairs,
        unitarity_diagnostics,
    )


